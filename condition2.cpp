#include <iostream>
using namespace std;

int main ()
{
  int number = 7;

  switch(number) {
    case 1:
    cout << "Angka 1 \n";
    break;

    case 2:
    cout << "Angka 2 \n";
    break;

    case 3:
    cout << "Angka 3 \n";
    break;

    case 4:
    cout << "Angka 4 \n";
    break;

    default :
    cout << "Angka 5 \n";
  }

  return 0;
}


